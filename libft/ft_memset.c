#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	void	*res;

	res = str;
	i = 0;
	while (n > i)
	{
		*(char *)str = c;
		str ++;
		i++;
	}
	return (res);
}

